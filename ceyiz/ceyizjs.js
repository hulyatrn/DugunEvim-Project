$(function(){

    var kontrol=0;



    $("#yildiz i").mouseover(function(){

            var index=$(this).index();

            $("#yildiz i").css("color","#000")

            

            for(i=index;i>=0;i--){

                $("#yildiz i:eq("+i+")").css("color","#c71585");

            }

    })



    $("#yildiz i").mouseout(function(){

        if(kontrol==0){

            $("#yildiz i").css("color","#000");
        }

    })



    $("#yildiz i").click(function(){

        var index=$(this).index()+1;
        kontrol=1;

        alert(index+" yıldız verdiniz.");

    })
    

})







